export const detectLanguage = (text: string): { language: string; confidence: number } => {
  const words = text
    .toLowerCase()
    .replace(/[^a-zà-ÿ\s]/g, '')
    .split(/\s+/)
    .filter(Boolean);
  if (words.length === 0) return { language: 'es', confidence: 0 };

  const scores: Record<string, number> = {};
  for (const [lang, common] of Object.entries(COMMON_WORDS)) {
    let hits = 0;
    for (const w of words) {
      if (common.includes(w)) hits++;
    }
    scores[lang] = hits / Math.max(words.length, common.length * 0.05);
  }

  let bestLang = 'es';
  let bestScore = 0;
  for (const [lang, score] of Object.entries(scores)) {
    if (score > bestScore) {
      bestScore = score;
      bestLang = lang;
    }
  }

  // Normalize confidence
  const confidence = Math.min(1, bestScore * 3);
  return { language: bestLang, confidence };
};

/* ── Translation Providers ───────────────────────────────────────────────── */

const TRANSLATION_PROVIDER = process.env['TRANSLATION_PROVIDER'] ?? 'google';
const LIBRETRANSLATE_URL = process.env['LIBRETRANSLATE_URL'] ?? 'https://libretranslate.de';
const DEEPL_API_KEY = process.env['DEEPL_API_KEY'] ?? '';

export const isTranslationAvailable = (): boolean => true; // Always available with Google fallback

export const getSupportedLanguages = (): string[] => ['es', 'en', 'pt', 'fr', 'it', 'de'];

const translateLibre = async (text: string, target: string, source?: string): Promise<TranslationResult | null> => {
  try {
    const response = await fetch(`${LIBRETRANSLATE_URL}/translate`, {
      method: 'POST',
      headers: { 'content-type': 'application/json' },
      body: JSON.stringify({ q: text, source: source ?? 'auto', target, format: 'text' }),
    });
    if (!response.ok) return null;
    const data = (await response.json()) as {
      translatedText: string;
      detectedLanguage?: { language: string; confidence: number };
    };
    return {
      original: text,
      translated: data.translatedText,
      detectedLanguage: data.detectedLanguage?.language ?? source ?? 'unknown',
      confidence: data.detectedLanguage?.confidence ?? 0.8,
      provider: 'libretranslate',
    };
  } catch {
    return null;
  }
};

const translateDeepL = async (text: string, target: string, source?: string): Promise<TranslationResult | null> => {
  if (!DEEPL_API_KEY) return null;
  try {
    const body = new URLSearchParams({ text, target_lang: target.toUpperCase() });
    if (source) body.append('source_lang', source.toUpperCase());
    const response = await fetch('https://api-free.deepl.com/v2/translate', {
      method: 'POST',
      headers: {
        authorization: `DeepL-Auth-Key ${DEEPL_API_KEY}`,
        'content-type': 'application/x-www-form-urlencoded',
      },
      body,
    });
    if (!response.ok) return null;
    const data = (await response.json()) as {
      translations: Array<{ text: string; detected_source_language?: string }>;
    };
    const t = data.translations[0];
    if (!t) return null;
    return {
      original: text,
      translated: t.text,
      detectedLanguage: t.detected_source_language?.toLowerCase() ?? source ?? 'unknown',
      confidence: 0.9,
      provider: 'deepl',
    };
  } catch {
    return null;
  }
};

const translateGoogle = async (text: string, target: string, source?: string): Promise<TranslationResult | null> => {
  try {
    const sl = source ?? 'auto';
    const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=${sl}&tl=${target}&dt=t&q=${encodeURIComponent(text)}`;
    const response = await fetch(url, { headers: { 'user-agent': 'Mozilla/5.0' } });
    if (!response.ok) return null;
    const data = (await response.json()) as [Array<[string, string]>, string | null];
    const sentences = data[0];
    if (!sentences || !Array.isArray(sentences)) return null;
    const translated = sentences.map((s) => s[0]).join('');
    const detected = typeof data[1] === 'string' ? data[1] : (source ?? 'unknown');
    return {
      original: text,
      translated,
      detectedLanguage: detected,
      confidence: 0.75,
      provider: 'google',
    };
  } catch {
    return null;
  }
};

export const translate = async (text: string, targetLang: string, sourceLang?: string): Promise<TranslationResult> => {
  const target = LANG_MAP[targetLang] ?? targetLang;
  const source = sourceLang ? (LANG_MAP[sourceLang] ?? sourceLang) : undefined;

  let result: TranslationResult | null = null;

  if (TRANSLATION_PROVIDER === 'libretranslate') {
    result = await translateLibre(text, target, source);
    if (!result) result = await translateGoogle(text, target, source);
  } else if (TRANSLATION_PROVIDER === 'deepl') {
    result = await translateDeepL(text, target, source);
    if (!result) result = await translateGoogle(text, target, source);
  } else {
    result = await translateGoogle(text, target, source);
    if (!result) result = await translateLibre(text, target, source);
  }

  if (result) return result;

  // Ultimate fallback: return original
  return {
    original: text,
    translated: text,
    detectedLanguage: source ?? 'unknown',
    confidence: 0,
    provider: 'none',
  };
};

export const translateWithFallback = async (text: string, targetLang: string): Promise<TranslationResult> => {
  const detected = detectLanguage(text);
  return translate(text, targetLang, detected.language);
};

/* ── Pipeline helper ─────────────────────────────────────────────────────── */

export const processTranslatedCommand = async (
  transcript: string,
  processFn: (transcript: string) => Promise<{ response: string }>,
): Promise<{ originalResponse: string; translatedResponse: string; detectedLanguage: string }> => {
  const { language: detectedLang, confidence } = detectLanguage(transcript);
  log.debug(`[Translation] Detected language: ${detectedLang} (confidence: ${confidence.toFixed(2)})`);

  const internalLang = 'es';
  let commandText = transcript;

  // Translate to internal language if needed
  if (detectedLang !== internalLang && confidence > 0.3) {
    const t = await translate(transcript, internalLang, detectedLang);
    if (t.provider !== 'none') {
      commandText = t.translated;
      log.debug(`[Translation] Translated command: "${commandText.slice(0, 80)}..."`);
    }
  }

  // Process the command
  const result = await processFn(commandText);
  const originalResponse = result.response;

  // Translate response back to user's language
  if (detectedLang !== internalLang && confidence > 0.3) {
    const t = await translate(originalResponse, detectedLang, internalLang);
    if (t.provider !== 'none') {
      log.debug(`[Translation] Translated response to ${detectedLang}`);
      return { originalResponse, translatedResponse: t.translated, detectedLanguage: detectedLang };
    }
  }

  return { originalResponse, translatedResponse: originalResponse, detectedLanguage: detectedLang };
};
