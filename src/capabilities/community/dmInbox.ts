export const listConversations = (
  filters: {
    status?: ConversationStatus;
    intent?: ConversationIntent;
    priority?: ConversationPriority;
    assignedTo?: Conversation['assignedTo'];
    contactUsername?: string;
  } = {},
): Conversation[] => {
  let convs = loadInbox().conversations;
  if (filters.status) convs = convs.filter((c) => c.status === filters.status);
  if (filters.intent) convs = convs.filter((c) => c.intent === filters.intent);
  if (filters.priority) convs = convs.filter((c) => c.priority === filters.priority);
  if (filters.assignedTo) convs = convs.filter((c) => c.assignedTo === filters.assignedTo);
  if (filters.contactUsername) convs = convs.filter((c) => c.contact.username === filters.contactUsername);
  return convs.sort((a, b) => b.lastMessageAt.localeCompare(a.lastMessageAt));
};

export const getConversation = (conversationId: string): Conversation | null =>
  loadInbox().conversations.find((c) => c.id === conversationId) ?? null;

export const getInboxSnapshot = (): {
  totalActive: number;
  byStatus: Record<ConversationStatus, number>;
  byIntent: Record<string, number>;
  byPriority: Record<ConversationPriority, number>;
  needingResponse: number;
  escalatedToHuman: number;
  avgSentiment: number;
} => {
  const all = loadInbox().conversations;
  const byStatus: Record<string, number> = {};
  const byIntent: Record<string, number> = {};
  const byPriority: Record<string, number> = {};

  for (const c of all) {
    byStatus[c.status] = (byStatus[c.status] ?? 0) + 1;
    byIntent[c.intent] = (byIntent[c.intent] ?? 0) + 1;
    byPriority[c.priority] = (byPriority[c.priority] ?? 0) + 1;
  }

  const needingResponse = all.filter((c) => c.status === 'new' || c.status === 'in-progress').length;
  const escalated = all.filter((c) => c.status === 'escalated').length;
  const totalActive = all.filter((c) => c.status !== 'archived' && c.status !== 'resolved').length;
  const avgSentiment = all.length > 0 ? all.reduce((s, c) => s + c.sentiment, 0) / all.length : 0;

  return {
    totalActive,
    byStatus: byStatus as Record<ConversationStatus, number>,
    byIntent,
    byPriority: byPriority as Record<ConversationPriority, number>,
    needingResponse,
    escalatedToHuman: escalated,
    avgSentiment,
  };
};

