export const updateConversationStatus = (
  conversationId: string,
  status: ConversationStatus,
  note?: string,
): Conversation | null => {
  const store = loadInbox();
  const conv = store.conversations.find((c) => c.id === conversationId);
  if (!conv) return null;
  conv.status = status;
  if (status === 'resolved' && !conv.resolvedAt) conv.resolvedAt = new Date().toISOString();
  if (note) conv.notes.push(`[${new Date().toISOString()}] ${note}`);
  saveInbox(store);
  return conv;
};

export const addPromise = (conversationId: string, promise: string): Conversation | null => {
  const store = loadInbox();
  const conv = store.conversations.find((c) => c.id === conversationId);
  if (!conv) return null;
  conv.context.promisesMade.push(promise);
  saveInbox(store);
  return conv;
};

export const addObjection = (conversationId: string, objection: string): Conversation | null => {
  const store = loadInbox();
  const conv = store.conversations.find((c) => c.id === conversationId);
  if (!conv) return null;
  conv.context.objectionsRaised.push(objection);
  saveInbox(store);
  return conv;
};

export const escalateToHuman = (conversationId: string, reason: string): Conversation | null => {
  const store = loadInbox();
  const conv = store.conversations.find((c) => c.id === conversationId);
  if (!conv) return null;
  conv.status = 'escalated';
  conv.assignedTo = 'human';
  conv.notes.push(`[${new Date().toISOString()}] Escalado: ${reason}`);
  saveInbox(store);
  return conv;
};

