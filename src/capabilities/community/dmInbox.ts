/**
 * DM Inbox de FeedIA — bandeja de entrada con threading y contexto multi-turn.
 *
 * Reemplaza el inbox que un Community Manager humano gestiona. Mantiene el contexto
 * completo de cada conversación, clasifica por intención, prioriza, y sugiere/auto-genera
 * la próxima respuesta apropiada usando la voz de marca.
 */

