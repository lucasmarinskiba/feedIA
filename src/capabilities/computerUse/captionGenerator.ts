// ── 7. optimizeCaptionForAlgorithm ───────────────────────────────────────────

export const optimizeCaptionForAlgorithm = (caption: InstagramCaption, brand: BrandProfile): InstagramCaption => {
  let { main, hook, body, cta } = caption;
  const { hashtags, emojis } = caption;

  // 1. Ensure hook < 125 chars
  if (hook.length > 125) {
    hook = hook.slice(0, 122) + '...';
  }

  // 2. Remove any links (Instagram ignores them)
  const linkRegex = /https?:\/\/\S+/g;
  main = main.replace(linkRegex, '[link en bio]');
  hook = hook.replace(linkRegex, '[link en bio]');
  body = body.replace(linkRegex, '[link en bio]');
  cta = cta.replace(linkRegex, '[link en bio]');

  // 3. Ensure line break after hook so it's visually separated
  const hookInMain = main.indexOf(hook);
  if (hookInMain !== -1) {
    const afterHook = main.slice(hookInMain + hook.length);
    if (!afterHook.startsWith('\n\n')) {
      main = hook + '\n\n' + afterHook.replace(/^\n+/, '');
    }
  }

  // 4. Move hashtags to end (first-comment style note) — mark them for external handling
  const hashtagBlock = hashtags.join(' ');
  // Strip inline hashtags from body/hook
  const stripInlineHashtags = (text: string): string =>
    text
      .replace(/#\w+/g, '')
      .replace(/\s{2,}/g, ' ')
      .trim();

  if (brand.goals.primary === 'engagement' || brand.goals.primary === 'awareness') {
    // Keep hashtags inline for discovery
    if (!main.includes(hashtagBlock) && hashtagBlock) {
      main = `${stripInlineHashtags(main)}\n\n${hashtagBlock}`.trim();
    }
  } else {
    // Remove from main caption, note they should go to first comment
    main = stripInlineHashtags(main);
    if (!main.includes('[Hashtags → primer comentario]')) {
      main = `${main}\n\n[Hashtags → primer comentario]`.trim();
    }
  }

  // 5. Clamp total to 2200 chars
  if (main.length > 2200) {
    main = main.slice(0, 2197) + '...';
  }

  return {
    ...caption,
    main,
    hook,
    body,
    cta,
    hashtags,
    emojis,
    characterCount: main.length,
    readingTimeSeconds: readingTimeSeconds(main),
  };
};
