// ── 3. generateSubtitles ──────────────────────────────────────────────────────

export interface GenerateSubtitlesParams {
  transcript: string;
  durationMs: number;
  language?: string;
  wordsPerMinute?: number;
}

const splitIntoPhrases = (text: string, wordsPerPhrase: number): string[] => {
  const words = text.trim().split(/\s+/);
  const phrases: string[] = [];
  for (let i = 0; i < words.length; i += wordsPerPhrase) {
    phrases.push(words.slice(i, i + wordsPerPhrase).join(' '));
  }
  return phrases;
};

export const generateSubtitles = (params: GenerateSubtitlesParams): SubtitleTrack => {
  const { transcript, durationMs, language = 'es', wordsPerMinute = 150 } = params;

  const wordsPerMs = wordsPerMinute / (60 * 1000);
  const phrases = splitIntoPhrases(transcript, 6); // 5-8 words target, use 6

  const totalWords = transcript.trim().split(/\s+/).length;
  const msPerWord = totalWords > 0 ? durationMs / totalWords : 500;

  let cursor = 0;
  const lines: CaptionLine[] = phrases.map((phrase, _idx) => {
    const wordCount = phrase.split(/\s+/).length;
    const phraseDurationMs = Math.round(wordCount / wordsPerMs);
    const startMs = cursor;
    const endMs = Math.min(cursor + phraseDurationMs, durationMs);
    cursor = endMs;
    return { startMs, endMs, text: phrase };
  });

  // Adjust last line to exactly match durationMs
  if (lines.length > 0) {
    const last = lines[lines.length - 1];
    if (last) {
      lines[lines.length - 1] = { ...last, endMs: durationMs };
    }
  }

  // Suppress unused variable warning — msPerWord used above
  void msPerWord;

  return {
    lines,
    format: 'srt',
    language,
    autoGenerated: true,
  };
};

// ── 4. subtitleTrackToSRT ─────────────────────────────────────────────────────

const msToSRTTime = (ms: number): string => {
  const totalSeconds = Math.floor(ms / 1000);
  const milliseconds = ms % 1000;
  const hours = Math.floor(totalSeconds / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;
  return (
    [String(hours).padStart(2, '0'), String(minutes).padStart(2, '0'), String(seconds).padStart(2, '00')].join(':') +
    `,${String(milliseconds).padStart(3, '0')}`
  );
};

export const subtitleTrackToSRT = (track: SubtitleTrack): string =>
  track.lines
    .map((line, idx) => `${idx + 1}\n${msToSRTTime(line.startMs)} --> ${msToSRTTime(line.endMs)}\n${line.text}\n`)
    .join('\n');

// ── 5. subtitleTrackToVTT ─────────────────────────────────────────────────────

const msToVTTTime = (ms: number): string => {
  const totalSeconds = Math.floor(ms / 1000);
  const milliseconds = ms % 1000;
  const hours = Math.floor(totalSeconds / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;
  return (
    [String(hours).padStart(2, '0'), String(minutes).padStart(2, '00'), String(seconds).padStart(2, '00')].join(':') +
    `.${String(milliseconds).padStart(3, '0')}`
  );
};

export const subtitleTrackToVTT = (track: SubtitleTrack): string => {
  const header = `WEBVTT\n\n`;
  const body = track.lines
    .map((line, idx) => `${idx + 1}\n${msToVTTTime(line.startMs)} --> ${msToVTTTime(line.endMs)}\n${line.text}\n`)
    .join('\n');
  return header + body;
};

