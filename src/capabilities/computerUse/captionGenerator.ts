  "primary": { "main":"","hook":"","body":"","cta":"","hashtags":[],"emojis":[],"accessibility":{"altText":"","transcriptHint":""}},
  "casual":  { "main":"","hook":"","body":"","cta":"","hashtags":[],"emojis":[],"accessibility":{"altText":"","transcriptHint":""}},
  "formal":  { "main":"","hook":"","body":"","cta":"","hashtags":[],"emojis":[],"accessibility":{"altText":"","transcriptHint":""}},
  "short":   { "main":"","hook":"","body":"","cta":"","hashtags":[],"emojis":[],"accessibility":{"altText":"","transcriptHint":""}},
  "engagement":{ "main":"","hook":"","body":"","cta":"","hashtags":[],"emojis":[],"accessibility":{"altText":"","transcriptHint":""}}
