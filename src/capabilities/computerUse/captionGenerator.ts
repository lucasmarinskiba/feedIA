/**
 * Caption Generator — generación multi-propósito de captions y subtítulos para Instagram.
 *
 * Módulos:
 *  • generateCaption          — caption completo con hook/body/cta/hashtags via Claude
 *  • generateCaptionVariants  — 5 variantes para A/B testing
 *  • generateSubtitles        — subtítulos con time-code (puro, sin API)
 *  • subtitleTrackToSRT       — exporta SubtitleTrack a formato SRT
 *  • subtitleTrackToVTT       — exporta SubtitleTrack a WebVTT
 *  • generateAltText          — alt-text accesible para imágenes/carruseles
 *  • optimizeCaptionForAlgorithm — post-proceso determinista para el algoritmo de IG
 */

import Anthropic from '@anthropic-ai/sdk';
import { log } from '../../agent/logger.js';
import type { BrandProfile } from '../../config/types.js';

// ── Cliente Anthropic ─────────────────────────────────────────────────────────

const client = new Anthropic();

// SDK v0.32 no tipa aún `claude-opus-4-7` ni `adaptive thinking` como literales;
// casteamos sólo en el sitio exacto de `messages.create`.
// eslint-disable-next-line @typescript-eslint/no-explicit-any
const clientAny = client as any;

// ── Tipos públicos ────────────────────────────────────────────────────────────

export interface CaptionLine {
  startMs: number;
  endMs: number;
  text: string;
  speaker?: string;
}

export interface SubtitleTrack {
  lines: CaptionLine[];
  format: 'srt' | 'vtt' | 'json';
  language: string;
  autoGenerated: boolean;
}

export interface InstagramCaption {
  main: string;
  hook: string;
  body: string;
  cta: string;
  hashtags: string[];
  emojis: string[];
  characterCount: number;
  readingTimeSeconds: number;
  accessibility: {
    altText: string;
    transcriptHint: string;
  };
}

export interface CaptionVariants {
  primary: InstagramCaption;
  casual: InstagramCaption;
  formal: InstagramCaption;
  short: InstagramCaption;
  engagement: InstagramCaption;
}

// ── Helpers internos ──────────────────────────────────────────────────────────

const WORDS_PER_MINUTE = 150;

const readingTimeSeconds = (text: string): number => {
  const wordCount = text.trim().split(/\s+/).length;
  return Math.ceil(wordCount / (WORDS_PER_MINUTE / 60));
};

const extractEmojis = (text: string): string[] => {
  const matches = text.match(
    /[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{1F700}-\u{1F77F}\u{1F780}-\u{1F7FF}\u{1F800}-\u{1F8FF}\u{1F900}-\u{1F9FF}\u{1FA00}-\u{1FA6F}\u{1FA70}-\u{1FAFF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}]/gu,
  );
  return matches ?? [];
};

const buildCaptionFromText = (raw: string): InstagramCaption => {
  const lines = raw.trim().split('\n').filter(Boolean);
  const hook = lines[0]?.slice(0, 125) ?? '';
  const hashtagLine = [...lines].reverse().find((l: string) => l.startsWith('#')) ?? '';
  const hashtags = hashtagLine.match(/#\w+/g) ?? [];
  const bodyLines = lines.filter((l) => l !== hashtagLine && l !== lines[0]);
  const cta = bodyLines.at(-1) ?? '';
  const body = bodyLines.slice(0, -1).join('\n');
  const main = raw.slice(0, 2200);
  const emojis = extractEmojis(main);

  return {
    main,
    hook,
    body,
    cta,
    hashtags,
    emojis,
    characterCount: main.length,
    readingTimeSeconds: readingTimeSeconds(main),
    accessibility: {
      altText: '',
      transcriptHint: '',
    },
  };
};

const parseCaptionJson = (raw: string, fallbackRaw?: string): InstagramCaption => {
  const cleaned = raw
    .replace(/^```json\s*/i, '')
    .replace(/^```\s*/i, '')
    .replace(/```\s*$/i, '')
    .trim();
  try {
    const parsed = JSON.parse(cleaned) as Partial<InstagramCaption>;
    const main = (parsed.main ?? '').slice(0, 2200);
    return {
      main,
      hook: parsed.hook ?? main.slice(0, 125),
      body: parsed.body ?? '',
      cta: parsed.cta ?? '',
      hashtags: parsed.hashtags ?? [],
      emojis: parsed.emojis ?? extractEmojis(main),
      characterCount: main.length,
      readingTimeSeconds: readingTimeSeconds(main),
      accessibility: {
        altText: parsed.accessibility?.altText ?? '',
        transcriptHint: parsed.accessibility?.transcriptHint ?? '',
      },
    };
  } catch {
    return buildCaptionFromText(fallbackRaw ?? cleaned);
  }
};

// ── 1. generateCaption ────────────────────────────────────────────────────────

export interface GenerateCaptionParams {
  topic: string;
  format: 'reel' | 'carrusel' | 'post-imagen' | 'historia';
  hook: string;
  body: string;
  cta?: string;
  brand: BrandProfile;
}

export const generateCaption = async (params: GenerateCaptionParams): Promise<InstagramCaption> => {
  const { topic, format, hook, body, cta, brand } = params;
  const locale = brand.audience.locale ?? 'es-AR';
  const tones = brand.voice.tone.join(', ');
  const forbidden = brand.voice.forbidden.length ? brand.voice.forbidden.join(', ') : 'ninguna';
  const hashtagHints =
    Object.values(brand.hashtagPools ?? {})
      .flat()
      .slice(0, 15)
      .join(' ') || '#instagram';

  const systemPrompt = `Sos un experto copywriter de Instagram especializado en marcas argentinas.
Escribís en español rioplatense (${locale}) con tono: ${tones}.
Palabras/frases PROHIBIDAS: ${forbidden}.
Siempre estructurás: hook impactante → desarrollo → CTA → hashtags.
El hook debe provocar una emoción fuerte (pregunta disruptiva, dato sorprendente o número concreto).
Respondés SIEMPRE con JSON válido, sin markdown adicional.`;

  const userPrompt = `Generá un caption para Instagram.

Marca: ${brand.name} (${brand.niche})
Formato: ${format}
Tema: ${topic}
Hook sugerido: ${hook}
Desarrollo: ${body}
${cta ? `CTA sugerido: ${cta}` : ''}
Hashtags disponibles: ${hashtagHints}

Reglas:
- Hook < 125 caracteres (lo que ve el usuario antes de "más")
- Total ≤ 2200 caracteres
- Entre 5 y 15 hashtags relevantes
- Emojis estratégicos (no más de 8)
- Sin links (Instagram no los vuelve clicables)
- Salto de línea después del hook

Respondé SOLO con este JSON (sin markdown):
{
  "main": "texto completo del caption",
  "hook": "primeros 125 chars",
  "body": "desarrollo principal",
  "cta": "llamado a la acción",
  "hashtags": ["#tag1", "#tag2"],
  "emojis": ["emoji1", "emoji2"],
  "accessibility": {
    "altText": "descripción accesible de la visual",
    "transcriptHint": "pista de transcripción para reel"
  }
}`;

  log.info(`[CaptionGenerator] Generando caption para "${topic}" (${format})`);

  try {
    const response = await clientAny.messages.create({
      model: 'claude-opus-4-7',
      max_tokens: 16000,
      thinking: { type: 'adaptive', budget_tokens: 8000 },
      system: systemPrompt,
      messages: [{ role: 'user', content: userPrompt }],
    });

    const textBlock = (response.content as Array<{ type: string; text?: string }>).find((b) => b.type === 'text');
    const raw = textBlock?.text ?? '';
    const caption = parseCaptionJson(raw, raw);
    log.success(`[CaptionGenerator] Caption generado (${caption.characterCount} chars)`);
    return caption;
  } catch (err) {
    log.error(`[CaptionGenerator] Error generando caption: ${(err as Error).message}`);
    return buildCaptionFromText(`${hook}\n\n${body}\n\n${cta ?? ''}\n\n${hashtagHints}`);
  }
};

