export const pickAgentsForGoal = (goal, platform, max = 3) => {
  const g = String(goal || '').toLowerCase();
  const scored = GROWTH_SPECIALISTS.map((a) => {
    const kws = KEYWORD_MAP[a.id] || [];
    let s = 0;
    for (const kw of kws) if (g.includes(kw)) s += 2;
    if (platform && (a.platform === platform || a.platform === 'both')) s += 1;
    return { agent: a, score: s };
  }).sort((a, b) => b.score - a.score);
  const top = scored
    .filter((s) => s.score > 0)
    .slice(0, max)
    .map((s) => s.agent);
  if (top.length) return top;
  const def =
    platform === 'tiktok'
      ? ['tt-algorithm', 'hook-engineer', 'retention-architect']
      : platform === 'instagram'
        ? ['ig-algorithm', 'hook-engineer', 'save-bait']
        : ['hook-engineer', 'ig-algorithm', 'tt-algorithm'];
  return def.map((id) => findAgent(id)).filter(Boolean);
};

